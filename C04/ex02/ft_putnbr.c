#include <unistd.h>

void	ft_putnbr(int nb)
{
	int		b;
	long	a;

	a = nb;
	if (a < 0)
	{
		write (1, "-", 1);
		a = -a;
	}
	b = a % 10 + '0';
	if (a >= 10)
	{
		a /= 10;
		ft_putnbr (a);
	}
	write (1, &b, 1);
}
