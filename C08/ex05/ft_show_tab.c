#include <unistd.h>
#include "ft_stock_str.h"

struct	s_stock_str	*ft_strs_to_tab(int ac, char **av);

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write (1, &str[i], 1);
		i++;
	}
}

void	ft_putnbr(int nbr)
{
	long	nb;
	int		result;

	nb = nbr;
	if (nb < 0)
	{
		write (1, "-", 1);
		nb = -nb;
	}
	result = nb % 10 + '0';
	if (nb >= 10)
	{
		nb /= 10;
		ft_putnbr(nb);
	}
	write (1, &result, 1);
}

void	ft_show_tab(struct s_stock_str *par)
{
	int	i;

	i = 0;
	while (par[i].str)
	{
		ft_putstr(par[i].str);
		write (1, "\n", 1);
		ft_putnbr(par[i].size);
		write (1, "\n", 1);
		ft_putstr(par[i].copy);
		write (1, "\n", 1);
		i++;
	}
}
