int	ft_sqrt(int nb)
{
	unsigned int	i;
	unsigned int	nbr;

	i = 0;
	nbr = nb;
	while (i * i < nbr)
	{
		i++;
	}
	if (i * i == nbr)
		return (i);
	else
		return (0);
}
